package com.rays.servletJ;

public class AuthBean {
	
	private String email;
	private String pwd;
	
	public AuthBean() {}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	
	

}
